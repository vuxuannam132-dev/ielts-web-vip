// Helper formula to predictably select 4 random missions per day for a user
export function getDailyMissions(userId: string) {
    const d = new Date();
    // Unique seed for the day + user
    const dateStr = `${d.getFullYear()}${d.getMonth()}${d.getDate()}`;
    // Simple hash
    let hash = 0;
    const str = userId + dateStr;
    for (let i = 0; i < str.length; i++) {
        const char = str.charCodeAt(i);
        hash = (hash << 5) - hash + char;
        hash = hash & hash; // Convert to 32bit integer
    }

    // Pseudo random generator with that seed
    hash = Math.abs(hash);
    const selected = [];
    for (let i = 0; i < 4; i++) {
        // Pick random from 80
        const idx = (hash + i * 17) % dailyMissions.length;
        selected.push(dailyMissions[idx]);
    }
    return selected;
}
